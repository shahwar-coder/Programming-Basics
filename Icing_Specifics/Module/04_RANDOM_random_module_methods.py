'''
Q4. How do `choice()`, `choices()`, and `sample()` differ?
Ans. `choice()` picks one item, `choices()` allows repetition, `sample()` does not.
'''
# Example
items = ["a", "b", "c"]
random.choice(items)        # one element
random.choices(items, k=2)  # may repeat
random.sample(items, 2)     # no repetition


'''
Q5. What does `shuffle()` do?
Ans. It randomly rearranges the elements of a list in-place.
'''
# Example
nums = [1, 2, 3, 4]
random.shuffle(nums)
# nums order changed


'''
Q6. What is `seed()` and why is it useful?
Ans. `seed()` fixes randomness so results can be reproduced.
'''
# Example
random.seed(42)
random.randint(1, 10)


